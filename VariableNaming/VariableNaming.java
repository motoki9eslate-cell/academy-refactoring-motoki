public class VariableNaming {

  public void calculateNumbers() {
    /*
     * パターン1
     */
    int firstNumber = 10;
    int secondNumber = 20;

    /*
     * パターン2
     */
    int width = 5;
    int height = 3;
    /*
     * パターン3
     */
    int fifthNumber = 100;

    int totalResult = firstNumber + secondNumber;
    int sum = width + height + fifthNumber;

    System.out.println(totalResult);
    System.out.println(sum);
  }
}
